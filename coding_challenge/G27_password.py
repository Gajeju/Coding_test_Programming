# A 회사 총무부서가 분주합니다. 중요한 파일이 모두 들어있는 컴퓨터의 비밀번호를 까먹었기 때문입니다.
# 다행히 책상에는 전임자가 놓고간 힌트가 담긴 쪽지가 놓여있습니다. 쪽지의 내용은 이러합니다. 
# '비밀번호는 쉴새없이 변하며, 컴퓨터 화면에는 매 번 일련의 문자들이 주어집니다. 
# 단 컴퓨터 화면에서 알파벳 c가 입력될 때까지 순서대로 주어진 알파벳들이 비밀번호입니다.' 
# 머리가 좋은 김대리는 바로 이해를 하고 암호를 풀었다. 김대리가 사용한 방법을 프로그램으로 만드세요

import sys

password = list(sys.stdin.readline().split())

for c in password:
    if c == 'c':
        print(c)
        break
    print(c, end=' ')